    private void jTablePelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePelangganMouseClicked
        // TODO add your handling code here:
        String Hobi;
        if(evt.getClickCount()==1){
            int row = jTablePelanggan.getSelectedRow();
            idPelanggan = Integer.parseInt(tableModel.getValueAt(row, 0).toString());
            jTextFieldNama.setText(tableModel.getValueAt(row, 1).toString());
            if(tableModel.getValueAt(row, 2).toString().equals("Laki-Laki")){
                jRadioButtonL.setSelected(true);
            }else{
                jRadioButtonP.setSelected(true);
            }
            jComboBoxAgama.setSelectedItem(tableModel.getValueAt(row, 3).toString());
            jTextAreaAlamat.setText(tableModel.getValueAt(row, 4).toString());
            jTextFieldPhone.setText((tableModel).getValueAt(row, 5).toString());
            Hobi = tableModel.getValueAt(row, 6).toString();
            if(Hobi.equals("Olahraga,")) {
                jCheckBox1.setSelected(true);
                jCheckBox2.setSelected(false);
                jCheckBox3.setSelected(false);
                jCheckBox4.setSelected(false);   
            } else if (Hobi.equals("Musik,")) {
                jCheckBox1.setSelected(false);
                jCheckBox2.setSelected(true);
                jCheckBox3.setSelected(false);
                jCheckBox4.setSelected(false);
            } else if (Hobi.equals("Membaca,")) {
                jCheckBox1.setSelected(false);
                jCheckBox2.setSelected(false);
                jCheckBox3.setSelected(true);
                jCheckBox4.setSelected(false);
            } else if (Hobi.equals("Melukis,")) {
                jCheckBox1.setSelected(false);
                jCheckBox2.setSelected(false);
                jCheckBox3.setSelected(false);
                jCheckBox4.setSelected(true);
            }
    }

    }//GEN-LAST:event_jTablePelangganMouseClicked

