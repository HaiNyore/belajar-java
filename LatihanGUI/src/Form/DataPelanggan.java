    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
        String jk, Hobi;
        if(jRadioButtonL.isSelected()){ jk = "Laki-Laki"; } else { jk = "Perempuan"; }
        Hobi ="";
        if(jCheckBox1.isSelected()){ Hobi += jCheckBox1.getText() + ","; }
        if(jCheckBox2.isSelected()){ Hobi += jCheckBox2.getText() + ","; }
        if(jCheckBox3.isSelected()){ Hobi += jCheckBox3.getText() + ","; }
        if(jCheckBox4.isSelected()){ Hobi += jCheckBox4.getText() + ","; }
        try{
            conn = ctrlKoneksi.mariaDB();
            String sql = "INSERT INTO datapelanggan (Nama,Jenis_kelamin,Agama,Alamat,Phone,Hobi) VALUES ("
            + "'"+jTextFieldNama.getText()+"',"
            + "'"+jk+"',"
            + "'"+jComboBoxAgama.getSelectedItem()+"',"
            + "'"+jTextAreaAlamat.getText()+"',"
            + "'"+jTextFieldPhone.getText()+"',"
            + "'"+Hobi+"'"
            + ")";
            pst = conn.prepareStatement(sql);
            pst.execute();
            pst.close();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            getDataPelanggan();
        } catch(Exception e){
            jTextPane1.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButtonSimpanActionPerformed

